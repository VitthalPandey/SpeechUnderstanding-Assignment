import numpy as np
import matplotlib.pyplot as plt
from datasets import load_dataset

# ===============================
# LOAD AUDIO
# ===============================
def load_sample_audio():
    dataset = load_dataset("librispeech_asr", "clean", split="validation[:1]")
    audio = dataset[0]["audio"]["array"]
    sr = dataset[0]["audio"]["sampling_rate"]
    return sr, audio


# ===============================
# WINDOW FUNCTIONS
# ===============================
def apply_window(signal, window_type):
    N = len(signal)

    if window_type == "hamming":
        return signal * np.hamming(N)
    elif window_type == "hanning":
        return signal * np.hanning(N)
    else:
        return signal  # rectangular


# ===============================
# FFT
# ===============================
def compute_fft(signal):
    return np.fft.fft(signal)


# ===============================
# SPECTRAL LEAKAGE (PROPER)
# ===============================
def compute_leakage(signal):
    spectrum = np.abs(compute_fft(signal))**2

    peak_idx = np.argmax(spectrum)
    main_energy = spectrum[peak_idx]

    total_energy = np.sum(spectrum)

    leakage = total_energy - main_energy
    leakage_ratio = leakage / total_energy

    return leakage_ratio


# ===============================
# SNR
# ===============================
def compute_snr(clean, noisy):
    noise = noisy - clean
    snr = 10 * np.log10(np.sum(clean**2) / (np.sum(noise**2) + 1e-10))
    return snr


# ===============================
# ANALYSIS FUNCTION
# ===============================
def analyze_windows(signal):
    windows = ["rectangular", "hamming", "hanning"]

    leakage_vals = []
    snr_vals = []

    for w in windows:
        windowed = apply_window(signal, w)

        # Leakage
        leakage = compute_leakage(windowed)

        # Add controlled noise
        noise = 0.01 * np.random.randn(len(signal))
        noisy = windowed + noise

        snr = compute_snr(windowed, noisy)

        leakage_vals.append(leakage)
        snr_vals.append(snr)

        print(f"{w.upper()} → Leakage: {leakage:.6f}, SNR: {snr:.2f} dB")

    return windows, leakage_vals, snr_vals


# ===============================
# PLOTS (REPORT QUALITY)
# ===============================
def plot_results(windows, leakage_vals, snr_vals):

    # Leakage plot
    plt.figure(figsize=(8, 4))
    plt.bar(windows, leakage_vals)
    plt.title("Spectral Leakage Comparison")
    plt.xlabel("Window Type")
    plt.ylabel("Leakage Ratio")
    plt.grid(axis='y')
    plt.show()

    # SNR plot
    plt.figure(figsize=(8, 4))
    plt.bar(windows, snr_vals)
    plt.title("Signal-to-Noise Ratio (SNR) Comparison")
    plt.xlabel("Window Type")
    plt.ylabel("SNR (dB)")
    plt.grid(axis='y')
    plt.show()


# ===============================
# RUN
# ===============================
sr, audio = load_sample_audio()

# Use a short segment for fair comparison
signal = audio[:2048]

windows, leakage_vals, snr_vals = analyze_windows(signal)

plot_results(windows, leakage_vals, snr_vals)

