def load_sample_audio():
    dataset = load_dataset("librispeech_asr", "clean", split="validation[:1]")
    audio = dataset[0]["audio"]["array"]
    sr = dataset[0]["audio"]["sampling_rate"]
    return sr, audio


# ===============================
# PRE-EMPHASIS
# ===============================
def pre_emphasis(signal, alpha=0.97):
    return np.append(signal[0], signal[1:] - alpha * signal[:-1])


# ===============================
# FRAMING
# ===============================
def framing(signal, sr, frame_size=0.025, frame_stride=0.01):
    frame_length = int(frame_size * sr)
    frame_step = int(frame_stride * sr)

    signal_length = len(signal)
    num_frames = int(np.ceil(float(np.abs(signal_length - frame_length)) / frame_step))

    pad_signal_length = num_frames * frame_step + frame_length
    pad_signal = np.append(signal, np.zeros(pad_signal_length - signal_length))

    indices = np.tile(np.arange(0, frame_length), (num_frames, 1)) + \
              np.tile(np.arange(0, num_frames * frame_step, frame_step), (frame_length, 1)).T

    return pad_signal[indices.astype(np.int32, copy=False)]


# ===============================
# CEPSTRUM COMPUTATION
# ===============================
def compute_cepstrum(frame):
    spectrum = np.fft.fft(frame)
    log_spectrum = np.log(np.abs(spectrum) + 1e-10)
    cepstrum = np.fft.ifft(log_spectrum).real
    return cepstrum


# ===============================
# VOICED / UNVOICED CLASSIFICATION
# ===============================
def classify_frame(cepstrum, threshold=0.02):
    # Low quefrency → vocal tract (envelope)
    low_q = np.mean(np.abs(cepstrum[:20]))

    # High quefrency → pitch periodicity
    high_q = np.mean(np.abs(cepstrum[20:100]))

    if high_q > threshold:
        return 1  # voiced
    else:
        return 0  # unvoiced


# ===============================
# SEGMENT SIGNAL
# ===============================
def segment_signal(signal, sr):
    frames = framing(signal, sr)
    labels = []

    for frame in frames:
        cep = compute_cepstrum(frame)
        label = classify_frame(cep)
        labels.append(label)

    return np.array(labels), frames


# ===============================
# VISUALIZATION (VERY IMPORTANT)
# ===============================
def plot_voiced_unvoiced(signal, sr, labels, frame_size=0.025, frame_stride=0.01):

    frame_length = int(frame_size * sr)
    frame_step = int(frame_stride * sr)

    time_axis = np.arange(len(signal))

    plt.figure(figsize=(12, 4))
    plt.plot(time_axis, signal, label="Speech Signal")

    for i, label in enumerate(labels):
        if label == 1:
            start = i * frame_step
            end = start + frame_length
            plt.axvspan(start, end, alpha=0.2)

    plt.title("Voiced (Highlighted) vs Unvoiced Segments")
    plt.xlabel("Samples")
    plt.ylabel("Amplitude")
    plt.legend()
    plt.tight_layout()
    plt.show()


# ===============================
# RUN
# ===============================
sr, audio = load_sample_audio()

# Pre-emphasis improves clarity
audio = pre_emphasis(audio)

labels, frames = segment_signal(audio, sr)

print("Total Frames:", len(labels))
print("Voiced Frames:", np.sum(labels))
print("Unvoiced Frames:", len(labels) - np.sum(labels))

plot_voiced_unvoiced(audio, sr, labels)

