import numpy as np
import matplotlib.pyplot as plt
import scipy.fftpack as fftpack
from scipy.signal import spectrogram
from datasets import load_dataset

# ===============================
# STEP 0: LOAD DATASET
# ===============================
def load_sample_audio():
    dataset = load_dataset("librispeech_asr", "clean", split="validation[:1]")
    audio = dataset[0]["audio"]["array"]
    sr = dataset[0]["audio"]["sampling_rate"]
    return sr, audio


# ===============================
# STEP 1: PRE-EMPHASIS
# ===============================
def pre_emphasis(signal, alpha=0.97):
    return np.append(signal[0], signal[1:] - alpha * signal[:-1])


# ===============================
# STEP 2: FRAMING
# ===============================
def framing(signal, sr, frame_size=0.025, frame_stride=0.01):
    frame_length = int(frame_size * sr)
    frame_step = int(frame_stride * sr)

    signal_length = len(signal)
    num_frames = int(np.ceil(float(np.abs(signal_length - frame_length)) / frame_step))

    pad_signal_length = num_frames * frame_step + frame_length
    pad_signal = np.append(signal, np.zeros(pad_signal_length - signal_length))

    indices = np.tile(np.arange(0, frame_length), (num_frames, 1)) + \
              np.tile(np.arange(0, num_frames * frame_step, frame_step), (frame_length, 1)).T

    return pad_signal[indices.astype(np.int32, copy=False)]


# ===============================
# STEP 3: WINDOWING
# ===============================
def apply_window(frames, window_type="hamming"):
    if window_type == "hamming":
        window = np.hamming(frames.shape[1])
    elif window_type == "hanning":
        window = np.hanning(frames.shape[1])
    else:
        window = np.ones(frames.shape[1])

    return frames * window


# ===============================
# STEP 4: FFT + POWER SPECTRUM
# ===============================
def power_spectrum(frames, NFFT=512):
    mag_frames = np.absolute(np.fft.rfft(frames, NFFT))
    return (1.0 / NFFT) * (mag_frames ** 2)


# ===============================
# STEP 5: MEL FILTERBANK
# ===============================
def mel_filterbank(pow_frames, sr, nfilt=26, NFFT=512):
    low_mel = 0
    high_mel = 2595 * np.log10(1 + (sr / 2) / 700)

    mel_points = np.linspace(low_mel, high_mel, nfilt + 2)
    hz_points = 700 * (10**(mel_points / 2595) - 1)

    bins = np.floor((NFFT + 1) * hz_points / sr)

    fbank = np.zeros((nfilt, int(NFFT / 2 + 1)))

    for m in range(1, nfilt + 1):
        f_m_minus = int(bins[m - 1])
        f_m = int(bins[m])
        f_m_plus = int(bins[m + 1])

        for k in range(f_m_minus, f_m):
            fbank[m - 1, k] = (k - bins[m - 1]) / (bins[m] - bins[m - 1])

        for k in range(f_m, f_m_plus):
            fbank[m - 1, k] = (bins[m + 1] - k) / (bins[m + 1] - bins[m])

    filter_banks = np.dot(pow_frames, fbank.T)
    filter_banks = np.where(filter_banks == 0, np.finfo(float).eps, filter_banks)

    return np.log(filter_banks)


# ===============================
# STEP 6: DCT → MFCC
# ===============================
def compute_mfcc(log_mel, num_ceps=13):
    return fftpack.dct(log_mel, type=2, axis=1, norm='ortho')[:, :num_ceps]


# ===============================
# FULL PIPELINE
# ===============================
def extract_mfcc():
    sr, audio = load_sample_audio()

    emphasized = pre_emphasis(audio)
    frames = framing(emphasized, sr)
    windowed = apply_window(frames, "hamming")
    power = power_spectrum(windowed)
    mel = mel_filterbank(power, sr)
    mfcc = compute_mfcc(mel)

    return mfcc, audio, sr


# ===============================
# VISUALIZATION
# ===============================
def plot_mfcc(mfcc):
    plt.figure(figsize=(10, 4))
    plt.imshow(mfcc.T, aspect='auto', origin='lower')
    plt.title("MFCC Features")
    plt.xlabel("Frames")
    plt.ylabel("Coefficients")
    plt.colorbar()
    plt.tight_layout()
    plt.show()


def plot_spectrogram(signal, sr):
    f, t, Sxx = spectrogram(signal, sr)

    plt.figure(figsize=(10, 4))
    plt.pcolormesh(t, f, 10 * np.log10(Sxx + 1e-10))
    plt.title("Spectrogram")
    plt.xlabel("Time")
    plt.ylabel("Frequency")
    plt.colorbar(label="Intensity (dB)")
    plt.tight_layout()
    plt.show()


# ===============================
# RUN EVERYTHING
# ===============================
mfcc, signal, sr = extract_mfcc()

print("MFCC shape:", mfcc.shape)

plot_mfcc(mfcc)
plot_spectrogram(signal, sr)

