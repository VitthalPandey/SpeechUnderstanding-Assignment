import numpy as np
import torch
import matplotlib.pyplot as plt
from datasets import load_dataset
from transformers import Wav2Vec2Processor, Wav2Vec2ForCTC

# ===============================
# LOAD AUDIO
# ===============================
def load_sample_audio():
    dataset = load_dataset("librispeech_asr", "clean", split="validation[:1]")
    audio = dataset[0]["audio"]["array"]
    sr = dataset[0]["audio"]["sampling_rate"]
    return sr, audio


# ===============================
# LOAD MODEL
# ===============================
def load_model():
    processor = Wav2Vec2Processor.from_pretrained("facebook/wav2vec2-base-960h")
    model = Wav2Vec2ForCTC.from_pretrained("facebook/wav2vec2-base-960h")
    return processor, model


# ===============================
# MODEL BOUNDARIES
# ===============================
def get_model_boundaries(audio, sr, processor, model):

    inputs = processor(audio, sampling_rate=sr, return_tensors="pt", padding=True)

    with torch.no_grad():
        logits = model(inputs.input_values).logits

    pred_ids = torch.argmax(logits, dim=-1)

    transcription = processor.batch_decode(pred_ids)[0]

    # Time mapping
    num_frames = logits.shape[1]
    duration = len(audio) / sr
    time_per_frame = duration / num_frames

    boundaries = np.arange(0, duration, time_per_frame)

    return boundaries, transcription


# ===============================
# MANUAL BOUNDARIES
# ===============================
def get_manual_boundaries(audio, sr, frame_stride=0.01):
    duration = len(audio) / sr
    return np.arange(0, duration, frame_stride)


# ===============================
# RMSE WITH ALIGNMENT (FIXED)
# ===============================
def compute_rmse(manual, model):

    # Make both same length using interpolation
    target_len = min(len(manual), len(model))

    manual_resampled = np.interp(
        np.linspace(0, len(manual), target_len),
        np.arange(len(manual)),
        manual
    )

    model_resampled = np.interp(
        np.linspace(0, len(model), target_len),
        np.arange(len(model)),
        model
    )

    rmse = np.sqrt(np.mean((manual_resampled - model_resampled) ** 2))

    return rmse, manual_resampled, model_resampled


# ===============================
# VISUALIZATION
# ===============================
def plot_boundaries(manual, model):

    plt.figure(figsize=(10, 4))

    plt.plot(manual, label="Manual Boundaries", marker='o', markersize=3)
    plt.plot(model, label="Model Boundaries", marker='x', markersize=3)

    plt.title("Boundary Alignment Comparison")
    plt.xlabel("Frame Index")
    plt.ylabel("Time (seconds)")
    plt.legend()
    plt.grid()
    plt.tight_layout()
    plt.show()


# ===============================
# RUN PIPELINE
# ===============================
sr, audio = load_sample_audio()

processor, model = load_model()

model_boundaries, transcription = get_model_boundaries(audio, sr, processor, model)

manual_boundaries = get_manual_boundaries(audio, sr)

rmse, manual_aligned, model_aligned = compute_rmse(manual_boundaries, model_boundaries)

print("Transcription:\n", transcription)
print("\nRMSE:", rmse)

plot_boundaries(manual_aligned, model_aligned)